#!/usr/bin/env bash
set -euo pipefail

ROOT="${ROOT:-/home/Wu_25R8111/rf_h_edit_project}"
cd "${ROOT}"

TASKS="${TASKS:-P1 P2 P3 P5 P6 P7 P8 P10 P11 P12 P13}"
METHODS="${METHODS:-M17 M18 M22}"
SEEDS="${SEEDS:-10}"
EDIT_SCALES="${EDIT_SCALES:-1.0 1.5}"
DEVICE="${DEVICE:-7}"
SKIP_EXISTING="${SKIP_EXISTING:-1}"
REGENERATE_MASKS="${REGENERATE_MASKS:-0}"
REUSE_SEMANTIC_MASKS="${REUSE_SEMANTIC_MASKS:-1}"
SEMANTIC_MASK_CACHE_METHOD="${SEMANTIC_MASK_CACHE_METHOD:-support_v3_fixed_policyv1_debug}"
ALLOW_MASK_DOWNLOAD="${ALLOW_MASK_DOWNLOAD:-0}"
SUPPORT_V3_TEMPORAL_AGGREGATION="${SUPPORT_V3_TEMPORAL_AGGREGATION:-mean}"

for scale in ${EDIT_SCALES}; do
  suffix="$(python3 - <<'PY' "${scale}"
import sys
print("_policyv1_edit" + str(int(round(float(sys.argv[1]) * 100))).zfill(3))
PY
)"
  METHOD_NAME_SUFFIX="${suffix}" \
  EDIT_STRENGTH_MULTIPLIER="${scale}" \
  TASKS="${TASKS}" METHODS="${METHODS}" SEEDS="${SEEDS}" DEVICE="${DEVICE}" \
  SKIP_EXISTING="${SKIP_EXISTING}" REGENERATE_MASKS="${REGENERATE_MASKS}" \
  REUSE_SEMANTIC_MASKS="${REUSE_SEMANTIC_MASKS}" SEMANTIC_MASK_CACHE_METHOD="${SEMANTIC_MASK_CACHE_METHOD}" \
  ALLOW_MASK_DOWNLOAD="${ALLOW_MASK_DOWNLOAD}" \
  SUPPORT_V3_TEMPORAL_AGGREGATION="${SUPPORT_V3_TEMPORAL_AGGREGATION}" \
    bash scripts/run_pretty_matrix.sh
done
